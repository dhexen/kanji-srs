export function t(lang: Lang, key: string): string {
  return translations[lang]?.[key] ?? translations['es'][key] ?? key
}

export function getStageName(level: number, lang: Lang): string {
  return t(lang, `stage_${Math.min(level, 7)}`)
}

// Returns the meaning field based on current language
export function getMeaning(item: { meaning: string; meaning_ca?: string; meaning_en?: string }, lang: Lang): string {
  if (lang === 'ca' && item.meaning_ca) return item.meaning_ca
  if (lang === 'en' && item.meaning_en) return item.meaning_en
  return item.meaning // es and ja both use Spanish meaning (ja has no translation)
}
